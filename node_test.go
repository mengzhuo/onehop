package onehop

import "testing"

func TestNewRemoteNodeFromByte(t *testing.T) {

	target := []byte{0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9,
		0xa, 0xb, 0xc, 0xd, 0xe, 0xf, 0x0,
		0x1,
		0x0a, 0x05, 0x04, 0xaf, // 10.5.4.175
		0xfa, 0x4b, // 64075
	}

	rn := NewRemoteNodeFromByte(target)
	n := rn.ToNode()

	if n.Addr.String() != "10.5.4.175:64075" {
		t.Errorf("IP parse error %s", n)
	}

}
